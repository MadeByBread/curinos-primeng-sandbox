/**
 * Resolve Transition token tables from the PrimeNG bridge (Layer 2).
 *
 * Reads:
 *   - src/styles/tokens/primeng/_index.scss  (canonical bridge declarations)
 *   - src/app/pages/transition/transition-sections.manifest.json  (portion labels + demo metadata)
 *
 * Writes:
 *   - src/app/pages/transition/bridge-mappings.generated.ts
 *
 * Usage: node scripts/extract-bridge-mappings.js
 */

const fs = require('fs');
const path = require('path');

const ROOT = path.join(__dirname, '..');
const BRIDGE_PATH = path.join(ROOT, 'src/styles/tokens/primeng/_index.scss');
const MANIFEST_PATH = path.join(ROOT, 'src/app/pages/transition/transition-sections.manifest.json');
const OUTPUT_PATH = path.join(ROOT, 'src/app/pages/transition/bridge-mappings.generated.ts');

function parseBridge(scss) {
  const vars = {};
  let currentGroup = 'unknown';

  scss.split('\n').forEach((line) => {
    const groupMatch = line.match(/^\s*\/\*\s*(.+?)\s*\*\/\s*$/);
    if (groupMatch) {
      currentGroup = groupMatch[1];
      return;
    }

    const varMatch = line.match(/^\s*(--primeng-[a-z0-9-]+):\s*(.+?);\s*$/);
    if (!varMatch) {
      return;
    }

    const [, name, rawValue] = varMatch;
    const value = rawValue.trim();
    const curinosMatch = value.match(/^var\((--curinos-[a-z0-9-]+)/);
    const bridgeAliasMatch = value.match(/^var\((--primeng-[a-z0-9-]+)/);

    vars[name] = {
      group: currentGroup,
      value,
      curinos: curinosMatch ? curinosMatch[1] : null,
      bridgeAlias: bridgeAliasMatch && !curinosMatch ? bridgeAliasMatch[1] : null,
      literal: !curinosMatch && !bridgeAliasMatch
    };
  });

  Object.keys(vars).forEach((name) => {
    const entry = vars[name];
    if (entry.bridgeAlias && vars[entry.bridgeAlias]) {
      entry.curinos = vars[entry.bridgeAlias].curinos;
    }
  });

  return vars;
}

function resolveCurinosLabel(bridgeVars, bridgeMap) {
  const curinos = [];
  bridgeVars.forEach((bridgeVar) => {
    const entry = bridgeMap[bridgeVar];
    if (!entry) {
      throw new Error(`Unknown bridge variable in manifest: ${bridgeVar}`);
    }
    if (entry.curinos && !curinos.includes(entry.curinos)) {
      curinos.push(entry.curinos);
    } else if (entry.literal && !curinos.includes(entry.value)) {
      curinos.push(entry.value);
    }
  });
  return curinos.join(', ');
}

function buildSections(manifest, bridgeMap) {
  return manifest.sections.map((section) => ({
    key: section.key,
    title: section.title,
    approach: section.approach,
    iframeHeight: section.iframeHeight,
    notes: section.notes || [],
    tokenMappings: section.mappings.map((row) => {
      const curinos = row.curinosNote
        ? `${resolveCurinosLabel(row.bridge, bridgeMap)} (${row.curinosNote})`
        : resolveCurinosLabel(row.bridge, bridgeMap);

      return {
        portion: row.portion,
        bridge: row.bridge.join(', '),
        curinos
      };
    })
  }));
}

function emitTypeScript(sections, iconMappings) {
  const payload = { sections, iconMappings };
  return `/* eslint-disable */
// GENERATED by scripts/extract-bridge-mappings.js — do not edit.
// Regenerate: npm run tokens:docs

export interface GeneratedTokenMapping {
  portion: string;
  bridge: string;
  curinos: string;
}

export interface GeneratedTransitionNote {
  title: string;
  body: string;
}

export interface GeneratedTransitionSection {
  key: string;
  title: string;
  approach: string;
  iframeHeight: number;
  tokenMappings: GeneratedTokenMapping[];
  notes: GeneratedTransitionNote[];
}

export interface GeneratedIconMapping {
  portion: string;
  bridge: string;
  curinos: string;
}

export const generatedTransitionData = ${JSON.stringify(payload, null, 2)} as {
  sections: GeneratedTransitionSection[];
  iconMappings: GeneratedIconMapping[];
};
`;
}

function main() {
  const bridgeScss = fs.readFileSync(BRIDGE_PATH, 'utf8');
  const manifest = JSON.parse(fs.readFileSync(MANIFEST_PATH, 'utf8'));
  const bridgeMap = parseBridge(bridgeScss);

  manifest.sections.forEach((section) => {
    section.mappings.forEach((row) => {
      row.bridge.forEach((bridgeVar) => {
        if (!bridgeMap[bridgeVar]) {
          throw new Error(`Section "${section.key}": bridge var ${bridgeVar} not found in _index.scss`);
        }
      });
    });
  });

  const sections = buildSections(manifest, bridgeMap);
  const output = emitTypeScript(sections, manifest.iconMappings || []);

  fs.writeFileSync(OUTPUT_PATH, output);
  console.log(`Wrote ${path.relative(ROOT, OUTPUT_PATH)} (${sections.length} sections)`);
}

main();
